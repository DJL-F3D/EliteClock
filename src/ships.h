struct ShipLore {
    const char *inservice;    // "3100 (Lave Orbital)"
    const char *dimensions;   // "65/30/65"
    const char *crew;         // "1-2"
    const char *drive;        // "Voltaire Whiplash"
};

struct ShipDef {
    const char  *name;
    ShipStats    stats;
    uint8_t      numVerts;
    uint8_t      numEdges;
    uint8_t      numFaces;
    const Vec3b *verts;
    const Edge  *edges;
    const Face  *faces;
    uint8_t      colour;      // index into SHIP_COLOURS[]
    const ShipLore *lore;
};

