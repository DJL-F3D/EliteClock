// =============================================================================
//  ships.h — Elite ship 3D geometry data
//  Vertices use signed 8-bit coords (-127..+127), matching the original Elite
//  blueprint scale. Faces carry a pre-computed normal for back-face culling
//  and flat shading.  Edge records store the two vertex indices plus the two
//  face indices on each side (used for visibility determination).
// =============================================================================

#include <stdint.h>

// ── Data structures ──────────────────────────────────────────────────────────

struct Vec3b { int8_t x, y, z; };          // compact vertex
struct Edge   { uint8_t v1, v2, f1, f2; }; // edge with adjacent faces

// Face: up to 8 vertex indices; n = actual count; normal computed at runtime
struct Face {
    uint8_t  verts[8];
    uint8_t  n;      // number of vertices in this face
    Vec3b    normal; // approximate face normal (pre-computed, unit-ish scale)
};

struct ShipStats {
    uint8_t  health;
    uint8_t  speed;
    uint8_t  lasers;
    uint8_t  missiles;
    uint16_t bounty;  // in tenths of a credit
    const char *role;
};

struct ShipDef {
    const char  *name;
    ShipStats    stats;
    uint8_t      numVerts;
    uint8_t      numEdges;
    uint8_t      numFaces;
    const Vec3b *verts;
    const Edge  *edges;
    const Face  *faces;
    uint8_t      colour;   // 0=cyan, 1=yellow, 2=green, 3=red, 4=white
};

